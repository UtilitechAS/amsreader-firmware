void test_dsmr_accepts_lf_and_crlf(void) {
    // A DSMR/P1 telegram must decode identically whether it uses CRLF (the P1
    // standard) or bare LF line endings.
    MeterConfig cfg; memset(&cfg, 0, sizeof(cfg));
    uint8_t crlf[4096]; int nc = harness_load_fixture("test/payloads/kamstrup/gh578-1.txt", crlf, sizeof(crlf));
    TEST_ASSERT_GREATER_THAN(0, nc);

    // Build an LF-only copy by dropping carriage returns. The DSMR CRC covers
    // the telegram bytes (line endings included), so a real LF meter computes
    // its CRC over LF content — recompute it here to make a valid LF telegram.
    uint8_t lf[4096]; int nl = 0;
    for (int i = 0; i < nc; i++) if (crlf[i] != '\r') lf[nl++] = crlf[i];
    TEST_ASSERT_TRUE(nl < nc);   // the fixture really was CRLF
    int bang = -1;
    for (int i = 0; i < nl; i++) if (lf[i] == '!') { bang = i; break; }
    TEST_ASSERT_GREATER_THAN(0, bang);
    char crc[5];
    snprintf(crc, sizeof(crc), "%04X", crc16(lf, bang + 1));
    memcpy(lf + bang + 1, crc, 4);   // overwrite the 4 CRC hex chars

    AmsData* a = harness_decode(crlf, (uint16_t)nc, &cfg, NULL, NULL);
    AmsData* b = harness_decode(lf,   (uint16_t)nl, &cfg, NULL, NULL);
    TEST_ASSERT_NOT_NULL(a);
    TEST_ASSERT_NOT_NULL_MESSAGE(b, "LF-only DSMR telegram failed to decode");
    TEST_ASSERT_EQUAL(a->getListType(), b->getListType());
    TEST_ASSERT_EQUAL(a->getActiveImportPower(), b->getActiveImportPower());
    TEST_ASSERT_FLOAT_WITHIN(0.01, a->getL1Voltage(), b->getL1Voltage());
    delete a; delete b;
}

